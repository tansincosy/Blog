import type { NextPage } from "next";
import { GetStaticProps } from "next";
import Head from "next/head";
import { readdirSync, readFileSync } from "fs";
import matter from "gray-matter";
import Link from "next/link";
import { useEffect } from "react";
import {
  applyTheme,
  argbFromHex,
  themeFromSourceColor,
} from "@material/material-color-utilities/dist";
import { Button, Card, Layout } from "@/components";
import { Post } from "@/types/post";

const Home: NextPage<{ posts: Post[] }> = ({ posts }) => {
  useEffect(() => {
    const theme = themeFromSourceColor(argbFromHex("#f82506"));
    const systemDark = window.matchMedia(
      "(prefers-color-scheme: dark)"
    ).matches;
    applyTheme(theme, { target: document.body, dark: systemDark });
  }, []);

  return (
    <>
      <Head>
        <title>Blog</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <main className="container mx-auto flex flex-wrap justify-center items-stretch">
          {posts.map((post) => {
            return (
              <Card
                key={post.title}
                type="filled"
                className="flex basis-80 m-5 z-10 overflow-hidden flex-col shrink"
              >
                <Link href={`/blog/${post.pathName}`}>
                  <a>
                    <div className="title">{post.title}</div>
                    <div>
                      {post.thumbnail && (
                        <img
                          src={post.thumbnail}
                          alt={post.title}
                          height={200}
                          width={200}
                        ></img>
                      )}
                      <div>{post.abstract}</div>
                      <div>
                        {Array.isArray(post.tags) &&
                          post.tags.map((tag) => {
                            return <div key={tag}>{tag}</div>;
                          })}
                      </div>
                    </div>
                  </a>
                </Link>
              </Card>
            );
          })}
        </main>
      </Layout>
      <footer>footer</footer>
    </>
  );
};

export const getStaticProps: GetStaticProps<any, any, Post[]> = async () => {
  const fileNames = readdirSync("posts");
  const posts = fileNames.map((filename) => {
    const fileContent = readFileSync(`posts/${filename}`).toString();
    let pathName = filename.replace(".md", "");
    const { data } = matter(fileContent) || {};
    return {
      title: data.title || "",
      thumbnail: data.thumbnail || "",
      abstract: data.abstract || "",
      tags: data.tags || [],
      pathName,
    };
  }) as Post[];
  return {
    props: {
      posts: posts,
    },
  };
};

export default Home;
